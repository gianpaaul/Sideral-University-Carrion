     private void cargarCursosEnComboBox() {
        Conexion conexion = new Conexion();
        try (Connection con = conexion.conectar();
             Statement stmt = con.createStatement();
             ResultSet rs = stmt.executeQuery("SELECT nombre_curso FROM cursos")) {

            while (rs.next()) {
                jcurso.addItem(rs.getString("nombre_curso"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al cargar cursos: " + e.getMessage());
        } finally {
            conexion.desconectar();
        }
    }


