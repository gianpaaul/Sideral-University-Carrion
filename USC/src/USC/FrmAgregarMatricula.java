    private void agregursoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_agregursoActionPerformed
      String cursoSeleccionado = (String) jcurso.getSelectedItem();
        String codigoCurso = tfcodigo.getText();
        String docente = tfdocente.getText();

        if (cursoSeleccionado == null || cursoSeleccionado.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Por favor, seleccione un curso.");
            return;
        }

        if (docente == null || docente.isEmpty()) {
            JOptionPane.showMessageDialog(this, "El curso seleccionado no tiene docente asignado. Por favor, elija otro curso.");
            return;
        }

        Conexion conexion = new Conexion();
        try (Connection con = conexion.conectar();
             PreparedStatement verificarMatricula = con.prepareStatement(
                     "SELECT * FROM matriculas WHERE id_alumno = ? AND id_curso = ?");
             PreparedStatement registrarMatricula = con.prepareStatement(
                     "INSERT INTO matriculas (id_alumno, id_curso) VALUES (?, ?)");) {
            int idEstudiante = obtenerIdEstudiante(); 
            int idCurso = obtenerIdCurso(codigoCurso); 
            verificarMatricula.setInt(1, idEstudiante);
            verificarMatricula.setInt(2, idCurso);
            try (ResultSet rs = verificarMatricula.executeQuery()) {
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Ya estás matriculado en este curso.");
                    return;
                }
            }
            registrarMatricula.setInt(1, idEstudiante);
            registrarMatricula.setInt(2, idCurso);
            registrarMatricula.executeUpdate();
            JOptionPane.showMessageDialog(this, "¡Matrícula realizada con éxito!");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al matricularse: " + e.getMessage());
        } finally {
            conexion.desconectar();
        }
    }//GEN-LAST:event_agregursoActionPerformed

