        public void Agregarcurso(){
        String Docente = this.cbodocente.getSelectedItem().toString();
        java.lang.String[] parts = Docente.split("-");
        String Docente1 = parts[0];
        String curso = this.cboCurso.getSelectedItem().toString();
        java.lang.String[] parts2 = curso.split("-");
        String curso1 = parts2[0]; 
        try {
            if (Docente.equals("") || curso.equals("")){
                JOptionPane.showMessageDialog(null, "Debe Ingresar Datos");
            } else {
                String insertar = "INSERT INTO carga_lectiva (id_carga,id_Docente,id_Curso) VALUES (NULL,'" + Docente1 + "','" + curso1 + "')";
                Statement st = cn.conectar().createStatement();
                int rs = st.executeUpdate(insertar);
                if (rs > 0) {
                    JOptionPane.showMessageDialog(null, "Se agreg√≥ designo el curso correctamente.");                  
                } else {
                    JOptionPane.showMessageDialog(null, "No se pudo asignar el curso.");
                }
            }
        } catch (Exception e){
            e.printStackTrace();
        }     
    }
    void Eliminar() {
        
        int fila = Tablasignar.getSelectedRow();
        if (fila < 0) {
            JOptionPane.showMessageDialog(null,"curso no Seleccionado");
        } else {
            int id = Integer.parseInt((String)Tablasignar.getValueAt(fila,0).toString());
                try {
                Statement st = cn.conectar().createStatement();
               
                JOptionPane.showMessageDialog(null, "curso Eliminado");
                limpiarTabla();
                String eliminarusuario = "DELETE FROM carga_lectiva WHERE carga_lectiva.id_carga="+id;
                st.executeUpdate(eliminarusuario);
                }catch(Exception e){
                }  
        }
    }
