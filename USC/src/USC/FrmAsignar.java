    void limpiarTabla(){
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }
    }
    public void listarCursos(){
        try {
            limpiarTabla();
            String listar = "SELECT * FROM carga_lectiva ORDER BY id_carga";
            Statement st = cn.conectar().createStatement();
            ResultSet rs = st.executeQuery(listar);
            Object[] persona = new Object[3];
            modelo = (DefaultTableModel) this.Tablasignar.getModel();
            while (rs.next()) {
                persona[0] = rs.getString("id_carga");
                persona[1] = rs.getString("id_Docente");
                persona[2] = rs.getString("id_Curso");
             
                modelo.addRow(persona);
            }
            Tablasignar.setModel(modelo);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
