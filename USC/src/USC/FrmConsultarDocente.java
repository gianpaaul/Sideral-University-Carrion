    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
    try {
        String DNI = this.txtDNI.getText();
        String consultar = "SELECT * FROM docentes WHERE DNI = '"+DNI+"'";
        Statement st = cn.conectar().createStatement();
        ResultSet rs = st.executeQuery(consultar);
        if (rs.next()) {
            this.txtNombre.setText(rs.getString("Nombres"));
            this.txtApellidos.setText(rs.getString("Apellidos"));
            this.txtProfesion.setText(rs.getString("Profesion"));
            this.txtDepartamento.setText(rs.getString("Departamento"));
            this.txtProvincia.setText(rs.getString("Provincia"));
            this.txtCorreo.setText(rs.getString("Correo"));
            this.txtSexo.setText(rs.getString("Sexo"));
        } else {
            JOptionPane.showMessageDialog(this, "No se encontraron resultados para el DNI proporcionado.");
        }
        rs.close();
        st.close();
    } catch (SQLException ex) {
        // Mostrar el error para depuraci√≥n
        ex.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error al realizar la consulta: " + ex.getMessage());
    }
    }//GEN-LAST:event_btnConsultarActionPerformed

